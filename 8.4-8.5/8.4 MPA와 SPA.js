// 8.4 MPA와 SPA
// MPA (Multi Page Application)
// 단점
// 1. 페이지 이동 속도 느림
// 2. 화면이 깜빡이는 듯한 현상
// 3. 동일한 코드 중복 작성

// SSR (Server Side Rendering)
// MPA 형식에서 서버가 요청한 페이지를 렌더링 하는 방식
// 특징
// 1. 첫 페이지 로딩 시간 빠름
// 2. 검색 엔진 (SEO) 최적화
// 3. 서버 부하
// 4. 좋지 않은 사용 경험

// SPA (Single Page Application)
// MPA와 반대 개념으로 한 페이지에서 렌더링하는 방식

// CSR (Client Side Rendering)
// SPA 형식에서 브라우저 (client)가 요청한 페이지를 렌더링 하는 방식
// 특징
// 1. 서버 부하 감소
// 2. 좋은 사용 경험
// 3. SEO 문제 (최적화 안됨)
// 4. 초기 로드 시간 증가
